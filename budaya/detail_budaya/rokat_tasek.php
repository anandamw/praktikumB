                    Rokat Tasek secara harfiah berarti "ritual laut" (rokat = ritual, tasek = laut). Upacara ini bertujuan untuk mengungkapkan rasa syukur kepada Tuhan atas hasil laut yang melimpah dan memohon perlindungan serta keselamatan bagi para nelayan. Selain itu, Rokat Tasek juga merupakan doa agar alam laut tetap memberikan berkah dan terhindar dari bencana.

